<div class="slide">
	<h4>Psuedo-class</h4>
	<p>Psuedo-classes are added to selectors to describe a state or implicit property of the selected element</p>
</div>

<div class="slide">
	<h4>Psuedo-class - :link </h4>
	<p>Applies if the selected element is a link</p>
	<pre>
.big-text:link {
	color: hotpink;
}
	</pre>
</div>

<div class="slide">
	<h4>Psuedo-class - :hover</h4>
	<p>Applies if the cursor is hovering over the element<p>
	<pre>
a:hover {
	color: gold;
}
	</pre>
</div>

<div class="slide">
	<h4>Psuedo-class - :first-child </h4>
	<p>Applies if the element is the first child of its parent</p>
	<pre>
li:first-child {
	color: green;
}
	</pre>
</div>

<div class="slide">
	<h4>Psuedo-class - :focus</h4>
	<p>Applies if the element has focus<p>
	<pre>
.first-name:hover {
	color: red;
}
	</pre>
</div>

