	<h4>CSS - display</h4>
	<p>The <strong>display</strong> property is used to define how boxes are generated for an element. </p>
	<p>3 common properties:</p>
	<ul class="build">
		<li><strong>none</strong> - doesn't generate a box for the element(hidden)</li>
		<li><strong>inline</strong> - generates a box that can only contain other inline elements and can have elements next to them </li>
		<li><strong>block</strong> - generates a box that can contain other elements, but will not have any elements next to them</li>
		<li><p>More Information</p><p><a href="http://www.w3.org/TR/CSS2/visuren.html#display-prop">http://www.w3.org/TR/CSS2/visuren.html#display-prop</a><p></li>
	</ul>
