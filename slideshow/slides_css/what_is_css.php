	</ul>
</div>


<div class="slide section">
	<h4>CSS - <small>Basic Syntax</small></h4>
	<ul class="build no-bullet">
		<li>
			CSS is generally written like this:
			<pre>
selector {
    property-name: value;
    property2: value;
}</pre>
		</li>
		<li>
			can also be written on a single line:
			<pre>
selector {property-name: value;property2: value;}</pre>
		</li>
	</ul>
</div>

